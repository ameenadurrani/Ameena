#basic printing
print("Hello,World!")
print("Ameena!")

#printing multiple items
print("Ameena Durrani")
print("1,2,3")

#printing special characters
print("Hello\nWorld")
print("Hello\tWorld")

#using sep parameter
print('apple', 'banana', 'Mango', sep=',')
print(1, 2, 3, sep='-')

#using end parameter
print('Hello', end=' ')
print('World')
print(1, end='')
print(2)

#escape characters
print('He said, "Hello!"')
print('This is a blackslash: \\')

#Combining Text and Numbers
age = 20
print(f'I am {age} years old')
number = 5
print(f'The number is {number}')

#Basic Loop for Printing
for i in range(1, 6):
    print(i)





